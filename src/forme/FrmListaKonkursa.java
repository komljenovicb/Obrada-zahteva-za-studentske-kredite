    private void dajKonkurse() {

        ArrayList<Konkurs> konkursi = new ArrayList<>();
        TransferObjekatZahtev tz = new TransferObjekatZahtev();
        tz.setOperacija(Operacija.OPERACIJA_VRATI_KONKURSE);
        KomunikacijaSaServerom.getInstanca().posajiZahtev(tz);

        TransferObjekatOdgovor too = (TransferObjekatOdgovor) KomunikacijaSaServerom.getInstanca().primiOdgovor();
        konkursi = (ArrayList<Konkurs>) too.getRezultat();
        System.out.println("Primljen odgovor servera: ");

        TabelaKonkursa tk = new TabelaKonkursa(konkursi);
        tabelaKonkursa.setModel(tk);

    }

