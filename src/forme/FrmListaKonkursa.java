    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int indeks = tabelaKonkursa.getSelectedRow();
        if(indeks != -1) {
            
            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Operacija.OBRISI_KONKURS);
            
            TabelaKonkursa tk = (TabelaKonkursa) tabelaKonkursa.getModel();
            Konkurs konkursZaBrisanje = tk.vratiKonkurs(indeks);
            toz.setZahtev(konkursZaBrisanje);
            KomunikacijaSaServerom.getInstanca().posajiZahtev(toz);
            
            tk.obrisiRed(indeks);
        } else {
            JOptionPane.showMessageDialog(this, "Niste izabrali red!");
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        
        int izabranIndeks = tabelaKonkursa.getSelectedRow();
        
        if(izabranIndeks == -1) {
            JOptionPane.showMessageDialog(this, "Morate odabrati red!");
        }
        
        TabelaKonkursa tk = (TabelaKonkursa) tabelaKonkursa.getModel();
        Konkurs k = tk.vratiKonkurs(izabranIndeks);

        this.dispose();
        FrmZaIzmenuKonkursa frm = new FrmZaIzmenuKonkursa();
        frm.popuniPolja(k);
        frm.setVisible(true);
        
    }//GEN-LAST:event_btnIzmeniActionPerformed

